export function ThemeHeader(theme) {
    return (
        {
            color : theme.secondary,
        }
    )
}

export function ThemeNavHeader(theme) {
    return (
        {
            background: theme.primary
        }
    )
}

export function ThemeImgHeader(theme) {
    return (
        {
            color: theme.secondary,
        }
    )
}

export function ThemeAHeader(theme) {
    return (
        {
            color : theme.secondary,
            background: theme.hover.primary,
        }
    )
}

